    private void jButton_ExaminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_ExaminarActionPerformed
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("Formatos de archivos(*.JPG;+.JPEG)","jpg","jp√©g");
        JFileChooser archivo=new JFileChooser();
        archivo.addChoosableFileFilter(filtro);
        archivo.setDialogTitle("Abrir Archivo");    
        File ruta=new File("C:/Users/User/Pictures");
        archivo.setCurrentDirectory(ruta);
        int ventana=archivo.showOpenDialog(null);
        if(ventana == JFileChooser.APPROVE_OPTION)
        {
            File file=archivo.getSelectedFile();
            try {
                byte[] fileContent = Files.readAllBytes(file.toPath());
                estudiante.setFoto(fileContent);
                System.out.println(file.toPath());
            } catch (IOException ex) {
                Logger.getLogger(Administracion.class.getName()).log(Level.SEVERE, null, ex);
            }
            Image foto=getToolkit().getImage(String.valueOf(file));
            foto=foto.getScaledInstance(150, 150, Image.SCALE_DEFAULT);
            jLabel_Foto.setIcon(new ImageIcon(foto));
    
        }
    }//GEN-LAST:event_jButton_ExaminarActionPerformed

    private void jButton_LeerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_LeerActionPerformed
        if(!AC){
            estudiante = opera.buscarEstudiante(jTextField_Tarjeta.getText());
            if(estudiante != null){
                jTextField_Nombre.setText(estudiante.getNombre());
                jTextField_Apellido.setText(estudiante.getApellido());
                jTextField_Escuela.setText(estudiante.getEscuela());
                jTextField_DNI.setText(estudiante.getDni());
                jTextField_CUI.setText(estudiante.getCui());
                jTextField_Celular.setText(estudiante.getCelular()); 
                try {
                    jLabel_Foto.setIcon(opera.convBytetoImagenIcon(estudiante.getFoto()));
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(this, ex.getMessage());
                }
            }
        }
    }//GEN-LAST:event_jButton_LeerActionPerformed

