    private void jButton_CargarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CargarActionPerformed
        // TODO add your handling code here:
        
        estudiante.setTarjeta(jTextField_Tarjeta.getText());
        estudiante.setNombre(jTextField_Nombre.getText());
        estudiante.setApellido(jTextField_Apellido.getText());
        estudiante.setCui(jTextField_CUI.getText());
        estudiante.setDni(jTextField_DNI.getText());
        estudiante.setEscuela(jTextField_Escuela.getText());
        estudiante.setCelular(jTextField_Celular.getText());
        try {
            if(AC){
                opera.crearEstudiante(estudiante);
            }else{
                opera.actualizarEstudiante(estudiante);
            }
            JOptionPane.showMessageDialog(this, "Operacion Exitosa");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "No se pudo realizar la operacion.", "Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_jButton_CargarActionPerformed

