    private void jButton_PagarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_PagarActionPerformed
        
        try {
            PagoId idpago = new PagoId();
            idpago.setTarjeta(estudiante.getTarjeta());
            Pago pago = new Pago();
            pago.setId(idpago);
            pago.setFecha(new Date());
            pago.setDinero(Double.parseDouble(jTextField_SaldoPagar.getText()));
            opera.realizarPago(pago);
            JOptionPane.showMessageDialog(this, "Se realizo pago exitosamente");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton_PagarActionPerformed

