    public void respuesta(String numeroTarjeta){
        jTextField_Tarjeta.setText(numeroTarjeta);
        try {
            estudiante = opera.buscarEstudiante(numeroTarjeta);
            jTextField_Nombre.setText(estudiante.getNombre());
            jTextField_Apellido.setText(estudiante.getApellido());
            double saldoTotal= opera.saldoTotal(numeroTarjeta);
            int cantidadConsumo = opera.cantidadConsumo(numeroTarjeta);
            double miSaldo = saldoTotal-((cantidadConsumo)*COSTO);
            jTextField_Saldo.setText(""+miSaldo);
            //System.out.println("");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(),"ERROR",JOptionPane.ERROR_MESSAGE);
        }    
    }
